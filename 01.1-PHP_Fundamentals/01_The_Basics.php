<!--Php stands for Php: Hypertext Preprocessor. -->
<!-- It is a server-side language. -->

<!-- Php must run on a server either Locally or in a Live environment like; XAMPP. -->

<!-- Original Syntax: This is Standard Practice. -->
<?php
  // code  goe here.
?>

<!-- Short Syntax. -->
<?
  // code  goe here.
?>

<!-- Hello World -->
<?
 echo "Hello World!";
?>
<!-- Php  can be placed anywhere inside of a html element.-->
<!-- Example: -->
<div style='<?php echo "border: 1px solid #000"; ?>'>Black Border Box!</div>

<!-- Quick Echo - Must be enabled on the server .-->
<!-- Example: -->
<?="My Text Goes Here"; ?>
<!-- This will ONLY ever be used to echo .-->

<!-- Semi-Colins: -->
<!-- In Php  semi-colons i.e. ";" are mandatory. -->

